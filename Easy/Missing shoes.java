import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
	    Scanner sc = new Scanner(System.in);
	    int L =sc.nextInt();
	    int R =sc.nextInt();
	    
	    
	    if(L>=R){
	    System.out.println(L-R);
	    }
	    else{
	    System.out.println(R-L);
	    }
	    
	}
}
