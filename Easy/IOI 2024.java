import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sc = new Scanner(System.in);
		
		int x = sc.nextInt();
		
		if(x>=1 && x<=8){
		    System.out.println("YES");
		}
		else{
		    System.out.println("NO");
		}

	}
}
